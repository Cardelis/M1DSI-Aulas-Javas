
public class Data {
	private int dia;
	private int mes;
	private int ano;
	private int hora;
	private int min;
	private int seg;
	public Data(int dia, int mes, int ano) {
		super();
		this.dia = dia;
		this.mes = mes;
		this.ano = ano;
	}
	public Data(int dia, int mes, int ano, int hora, int min, int seg) {
		super();
		this.dia = dia;
		this.mes = mes;
		this.ano = ano;
		this.hora = hora;
		this.min = min;
		this.seg = seg;
	}
	
}
